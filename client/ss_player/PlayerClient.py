def can_set_position(our_board, position, NONE=0, US=1, ENEMY=4):
    row, col = position

    # Check if the position is empty
    if our_board[row][col] != NONE:
        return False

    # Check adjacent cells
    adjacent_directions = [[-1, 0], [1, 0], [0, -1], [0, 1]]
    for dr, dc in adjacent_directions:
        r, c = row + dr, col + dc
        if 0 <= r < len(our_board) and 0 <= c < len(our_board[0]) and our_board[r][c] == US:
            return False

    # Check diagonal cells
    diagonal_directions = [[-1, -1], [-1, 1], [1, -1], [1, 1]]
    check_flag = 0
    for dr, dc in diagonal_directions:
        r, c = row + dr, col + dc
        if 0 <= r < len(our_board) and 0 <= c < len(our_board[0]) and our_board[r][c] == US:
            check_flag += 1

    return check_flag > 0


